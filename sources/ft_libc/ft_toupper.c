#include <ft_libc.h>

#ifdef USE_LIBC

# include <ctype.h>

inline int	ft_toupper(int c)
{
	return (toupper(c));
}

#else

int			ft_toupper(int c)
{
	if (c >= 'a' && c <= 'z')
		return (c - 32);
	return (c);
}

#endif
