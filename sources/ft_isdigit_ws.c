#include <libft.h>

int	ft_isdigit_ws(char *s)
{
	size_t	i;

	i = 0;
	while (s[i])
	{
		if ((s[i] >= '0' && s[i] <= '9') || ft_isws(s[i]))
			i++;
		else
			return (1);
	}
	return (0);
}
